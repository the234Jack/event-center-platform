export function getVenueById(id: string): Venue | undefined {
  return venues.find((v) => v.id === id);
}

export function getFeaturedVenues(): Venue[] {
  return venues.filter((v) => v.featured);
}

export function getVenuesByCity(city: string): Venue[] {
  return venues.filter((v) => v.city.toLowerCase() === city.toLowerCase());
}

export function getSimilarVenues(id: string, city: string, limit = 3): Venue[] {
  return venues
    .filter((v) => v.id !== id && v.city === city)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}
